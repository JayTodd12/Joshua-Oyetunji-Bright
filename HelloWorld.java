public class HelloWorld {

    public static void main(String[] args) {

        String fullName = "Oyetunji Joshua Bright"; // String is used for text values such as names
        int age = 13; // int is used for whole numbers without decimals
        char gender = 'M'; // char is used to store a single character
        double height = 1.75; // double is used for decimal numbers like height
        double weight = 70.5; // double is used for decimal numbers like weight
        boolean isStudent = true; // boolean stores true or false values
        long phoneNumber = 233208548599L; // long is used for large whole numbers such as phone numbers
        String gradeLevel = "Level 200"; // String is used for text values
        String nationality = "Ghanaian"; // String is used for text values
        String university = "Koforidua Technical University"; // String is used for text values

        System.out.println("===== PERSONAL INFORMATION =====");
        System.out.println("Full Name: " + fullName);
        System.out.println("Age: " + age);
        System.out.println("Gender: " + gender);
        System.out.println("Height: " + height + " m");
        System.out.println("Weight: " + weight + " kg");
        System.out.println("Student Status: " + isStudent);
        System.out.println("Phone Number: " + phoneNumber);
        System.out.println("Grade/Level: " + gradeLevel);
        System.out.println("Nationality: " + nationality);
        System.out.println("University: " + university);
    }
}